n = int(input())  # 주어진 정수 N
dp = [0] * (n + 1)  # dp 배열을 사용하여 각 정수에 대한 최소 연산 횟수 저장

# dp[i]는 i를 1로 만드는 최소 연산 횟수를 저장
for i in range(2, n + 1):
    dp[i] = dp[i - 1] + 1  # 기본적으로 1을 빼는 연산을 한 경우부터 시작

    # 3으로 나누어 떨어지면 3으로 나누는 연산을 고려
    if i % 3 == 0:
        dp[i] = min(dp[i], dp[i // 3] + 1)

    # 2로 나누어 떨어지면 2로 나누는 연산을 고려
    if i % 2 == 0:
        dp[i] = min(dp[i], dp[i // 2] + 1)

# 최종적으로 dp[n]에는 N을 1로 만들기 위한 최소 연산 횟수가 저장됨
print(dp[n])
